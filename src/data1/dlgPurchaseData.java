    private double getGrandTotalbyIdPembelian(int idpembelian) throws SQLException {
        double grandtotal = 0;
        String sqlPenyewaLookup = "Select grandtotal from pembelian where idpembelian = ? ;";

        PreparedStatement pstPenyewaLookup = conn.prepareStatement(sqlPenyewaLookup);
        pstPenyewaLookup.setInt(1, idpembelian);

        ResultSet rsPenyewaLookup = pstPenyewaLookup.executeQuery();
        while (rsPenyewaLookup.next()) {
            grandtotal = rsPenyewaLookup.getDouble("grandtotal");
        }
        return grandtotal;
    }

    private String getNamebyIdSupplier(int idsupplier) throws SQLException {
        String name = "";
        String sqlPenyewaLookup = "Select namasupplier from datasupplier where idsupplier = ? ;";

        PreparedStatement pstPenyewaLookup = conn.prepareStatement(sqlPenyewaLookup);
        pstPenyewaLookup.setInt(1, idsupplier);

        ResultSet rsPenyewaLookup = pstPenyewaLookup.executeQuery();
        while (rsPenyewaLookup.next()) {
            name = rsPenyewaLookup.getString("namasupplier");
        }
        return name;
    }

    private void executeGrandTotal() {
        try {
            String sql = "SELECT sum(grandtotal) FROM pembelian;";
            PreparedStatement pstatement = conn.prepareStatement(sql);

            ResultSet rs = pstatement.executeQuery();
            if (rs.isBeforeFirst()) { // check is resultset not empty
                while (rs.next()) {
                    txtGrandTotal.setText(String.valueOf(rs.getDouble("sum(grandtotal)")));
                }
            } else {
                util.Sutil.msg(this, "Record Empty");
            }

            rs.close();
            pstatement.close();
        } catch (SQLException ex) {
            Logger.getLogger(dlgSalesData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    private void loadAllDatabase() {
        removeTableData();
        try {
            String sql = "SELECT * FROM pembelian;";
            PreparedStatement pstatement = conn.prepareStatement(sql);

            ResultSet rs = pstatement.executeQuery();
            if (rs.isBeforeFirst()) { // check is resultset not empty
                while (rs.next()) {
                    DefaultTableModel tableModel = (DefaultTableModel) tblPurchaseData.getModel();

                    int idsupplier = rs.getInt("idsupplier");
                    int idpembelian = rs.getInt("grandtotal");
                    Object data[] = {
                        rs.getInt("idpembelian"),
                        rs.getDate("tanggalpembelian"),
                        getNamebyIdSupplier(idsupplier),
                        getGrandTotalbyIdPembelian(idpembelian)
                    };
                    tableModel.addRow(data);
                }
            } else {
                util.Sutil.msg(this, "Record Empty");
            }

            rs.close();
            pstatement.close();
            executeGrandTotal();
        } catch (SQLException ex) {
            Logger.getLogger(dlgSalesData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

