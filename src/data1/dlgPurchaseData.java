    private void searchbyName() {
        try {
            removeTableData();
            String sql = "SELECT p.idpembelian, p.tanggalpembelian, ds.namasupplier, p.grandtotal FROM "
                    + "pembelian p inner join datasupplier ds on p.idsupplier = ds.idsupplier WHERE namasupplier like ?;s";

            PreparedStatement pstatement = conn.prepareStatement(sql);
            pstatement.setString(1, "%" + txtSearchCustomer.getText().trim() + "%");

            ResultSet rs = pstatement.executeQuery();
            if (rs.isBeforeFirst()) { // check is resultset not empty
                DefaultTableModel tableModel = (DefaultTableModel) tblPurchaseData.getModel();
                while (rs.next()) {
                    Object data[] = {
                        rs.getInt("idpembelian"),
                        rs.getDate("tanggalpembelian"),
                        rs.getString("namasupplier"),
                        rs.getDouble("grandtotal")
                    };
                    tableModel.addRow(data);
                }
            }

            rs.close();
            pstatement.close();
            executeGrandTotalSearch();
        } catch (SQLException ex) {
            System.out.println("Error:\n" + ex.getLocalizedMessage());
        }
    }

    private void executeGrandTotalSearch() {
        for (int i = 0; i < tblPurchaseData.getRowCount(); i++) {
            double total = 0;
            double Amount = (double) tblPurchaseData.getValueAt(i, 3);
            total = Amount + total;
            txtGrandTotal.setText(String.valueOf(total));
        }
    }

    private void removeTableData() {
        DefaultTableModel tableModel = (DefaultTableModel) tblPurchaseData.getModel();
        tableModel.setRowCount(0);
    }

